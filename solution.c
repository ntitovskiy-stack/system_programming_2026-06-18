/*Разработать программу solution, которая осуществляет поиск родительского PID текущего процесса в файловой системе proc и выводит найденное значение на консоль. 
Результат, возвращаемый функций должен быть выведен отдельной строкой(должен оканчиваться символом перевода строки \n) в stdio.*/

#include <stdio.h>

int main()
{
    // Открытие файла статистики текущего процесса
    FILE *f = fopen("/proc/self/stat", "r");
    // Проверка: успешно ли открыт файл
    if (f == NULL) {
        fprintf(stderr, "Ошибка: не удалось открыть /proc/self/stat\n");
        return 1;
    }
    // Переменные для хранения данных
    int pid, ppid;
    char comm[256];
    char state;
    // Считывание первых 4 полей: PID, имя команды, состояние, родительский PID
    if (fscanf(f, "%d %s %c %d", &pid, comm, &state, &ppid) != 4) {
        fprintf(stderr, "Ошибка: неверный формат данных в /proc/self/stat\n");
        fclose(f);
        return 1;
    }
    printf("%d\n", ppid);
    // Закрытие файла
    fclose(f);
    return 0;
}
